CREATE TABLE TEST_ID (

	ID				integer NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	CLMAP           INTEGER DEFAULT 0,
    FIRM            CHAR(18) DEFAULT '',
	DEPT            CHAR(18) DEFAULT '',
    TESTNR          CHAR(12) DEFAULT '',
	COURSE          CHAR(22) DEFAULT '',
    ROADCOND        CHAR(22) DEFAULT '',
	WEATHER         CHAR(22) DEFAULT '',
    COMM1           CHAR(62) DEFAULT '',
    COMM2           CHAR(62) DEFAULT '',
    COMM3           CHAR(62) DEFAULT '',
    DRIVER          CHAR(21) DEFAULT '',
    INFO            CHAR(100) DEFAULT ''
);

ALTER TABLE TEST_ID ADD CONSTRAINT testid_pk Primary Key (ID);
ALTER TABLE TEST_ID ADD CONSTRAINT testid_fk FOREIGN KEY(CLMAP) REFERENCES CLASSMAP (ID);



